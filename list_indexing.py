marks=[3,5,6,"Harry",True]

# POSITIVE INDEXING :-

print(marks[3])
print(marks[len(marks)-1])

"""
NEGATIVE INDEXING :-

----> The simple way to use negative indexing is to subtract that 
negative index from the length of the list

"""

print(marks[-3])   # ---> 5-3= 2nd index
print(marks[-1])   # ---> 5-1= 4th index